create or replace trigger trigger_durada_album_contingut
after insert or delete or update of alc_id_canco
on ALBUM_CONTINGUT
for each ROW
declare 
v_durada number(6);
v_tipus char(1);
begin
	if inserting or updating THEN
		select pro_tipus into v_tipus from PRODUCTE where pro_id = :new.alc_id_canco;
		if v_tipus = 'C' THEN
			select can_durada into v_durada from CANCO where can_id = :new.alc_id_canco;
			update album set alb_durada = alb_durada + v_durada where alb_id = :new.alc_id_album;
		end if;
	end if;	
	
	if deleting or updating THEN
		select pro_tipus into v_tipus from PRODUCTE where pro_id = :old.alc_id_canco;
		if v_tipus = 'C' THEN
			select can_durada into v_durada from CANCO where can_id = :old.alc_id_canco;
			update album set alb_durada = alb_durada - v_durada where alb_id = :old.alc_id_album;
		end if;
	end if;	
end;
