create or replace trigger trigger_durada_album_aft_update_dur_canco
after update of can_durada on CANCO
for each ROW
BEGIN

	update album
	set alb_durada = alb_durada - :old.can_durada + :new.can_durada
	where alb_id in (select alc_id_album
				   from album_contingut
				   where alc_id_canco = :old.can_id);
END;

