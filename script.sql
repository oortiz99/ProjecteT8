CREATE TABLE ESTIL --correcte
(
	est_id NUMBER(4) GENERATED ALWAYS AS IDENTITY,
	est_nom VARCHAR2(64) NOT NULL,
	CONSTRAINT pk_ESTIL PRIMARY KEY (est_id),
	CONSTRAINT un_est_nom UNIQUE (est_nom)
