create or replace trigger trigger_durada_llista_contingut
after insert or delete or update of lic_id_PRODUCTE
on LLISTA_CONTINGUT
for each ROW
declare 
v_durada number(6);
v_tipus char(1);
begin
	if inserting or updating THEN
		select pro_tipus into v_tipus from PRODUCTE where pro_id = :new.lic_id_PRODUCTE;
		if v_tipus = 'C' THEN
			select can_durada into v_durada from CANCO where can_id = :new.lic_id_PRODUCTE;
			update llista set lis_durada = lis_durada + v_durada where lis_id = :new.lic_id_llista;
		end if;
		if v_tipus = 'A' THEN
			select alb_durada into v_durada from album where alb_id = :new.lic_id_PRODUCTE;
			update llista set lis_durada = lis_durada + v_durada where lis_id = :new.lic_id_llista;
		end if;	
	end if;	

	if deleting or updating THEN
		select pro_tipus into v_tipus from PRODUCTE where pro_id = :old.lic_id_PRODUCTE;
		if v_tipus = 'C' THEN
			select can_durada into v_durada from CANCO where can_id = :old.lic_id_PRODUCTE;
			update llista set lis_durada = lis_durada - v_durada where lis_id = :old.lic_id_llista;
		end if;
		if v_tipus = 'A' THEN
			select alb_durada into v_durada from album where alb_id = :old.lic_id_PRODUCTE;
			update llista set lis_durada = lis_durada - v_durada where lis_id = :old.lic_id_llista;
		end if;	
	end if;	
end;

/

